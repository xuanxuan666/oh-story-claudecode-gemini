#!/usr/bin/env python3
"""Generate portable Codex repo-discovery shims in .agents/skills.

The canonical skills stay under skills/. A tracked shim directory works on Windows
without requiring Git symlink support while still giving Codex the documented
.agents/skills discovery surface.
"""
from __future__ import annotations

import argparse
import re
import shutil
import sys
from pathlib import Path


MARKER = "<!-- Generated by scripts/generate-codex-skill-shims.py; do not edit. -->"
FRONTMATTER_RE = re.compile(r"\A---\r?\n(.*?)\r?\n---\r?\n", re.DOTALL)


def canonical_skills(source: Path) -> dict[str, Path]:
    found: dict[str, Path] = {}
    for skill_md in sorted(source.glob("*/SKILL.md")):
        text = skill_md.read_text(encoding="utf-8")
        match = FRONTMATTER_RE.match(text)
        if not match:
            raise ValueError(f"{skill_md}: missing YAML frontmatter")
        name_match = re.search(r"(?m)^name:\s*[\"']?([^\"'\r\n]+)", match.group(1))
        if not name_match:
            raise ValueError(f"{skill_md}: missing name")
        name = name_match.group(1).strip()
        if name != skill_md.parent.name:
            raise ValueError(f"{skill_md}: name must match directory ({name!r})")
        found[name] = skill_md
    return found


def render(skill_md: Path, repo_root: Path) -> str:
    text = skill_md.read_text(encoding="utf-8")
    match = FRONTMATTER_RE.match(text)
    assert match is not None
    relative = skill_md.relative_to(repo_root).as_posix()
    return (
        "---\n"
        + match.group(1).replace("\r\n", "\n")
        + "\n---\n\n"
        + MARKER
        + "\n\n# Codex repository adapter\n\n"
        + f"Read the canonical skill file at `../../../{relative}` completely, then follow it. "
        + f"Resolve every relative script, reference, or asset path against `../../../{skill_md.parent.relative_to(repo_root).as_posix()}/`.\n"
    )


def expected_files(source: Path, destination: Path, repo_root: Path) -> dict[Path, str]:
    return {
        destination / name / "SKILL.md": render(path, repo_root)
        for name, path in canonical_skills(source).items()
    }


def check(expected: dict[Path, str], destination: Path) -> int:
    failures: list[str] = []
    actual = set(destination.glob("*/SKILL.md")) if destination.is_dir() else set()
    for path, content in expected.items():
        if not path.is_file():
            failures.append(f"missing: {path}")
        elif path.read_text(encoding="utf-8") != content:
            failures.append(f"stale: {path}")
    for path in sorted(actual - set(expected)):
        failures.append(f"unexpected: {path}")
    if failures:
        print("Codex skill shims are not synchronized:", file=sys.stderr)
        for failure in failures:
            print(f"- {failure}", file=sys.stderr)
        return 1
    print(f"OK: {len(expected)} Codex skill shims are synchronized")
    return 0


def generate(expected: dict[Path, str], destination: Path) -> int:
    if destination.exists() and not destination.is_dir():
        raise ValueError(f"{destination} must be a directory, not a file or symlink stub")
    destination.mkdir(parents=True, exist_ok=True)
    expected_dirs = {path.parent for path in expected}
    for child in sorted(destination.iterdir()):
        if child in expected_dirs:
            continue
        marker_file = child / "SKILL.md" if child.is_dir() else child
        if child.is_dir() and marker_file.is_file() and MARKER in marker_file.read_text(encoding="utf-8"):
            shutil.rmtree(child)
        else:
            raise ValueError(f"refusing to remove unmanaged path: {child}")
    for path, content in expected.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8", newline="\n")
    print(f"Generated {len(expected)} portable Codex skill shims in {destination}")
    return 0


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--source", default="skills")
    parser.add_argument("--dest", default=".agents/skills")
    parser.add_argument("--check", action="store_true")
    args = parser.parse_args()

    repo_root = Path(__file__).resolve().parent.parent
    source = (repo_root / args.source).resolve()
    destination = (repo_root / args.dest).resolve()
    expected = expected_files(source, destination, repo_root)
    return check(expected, destination) if args.check else generate(expected, destination)


if __name__ == "__main__":
    raise SystemExit(main())
